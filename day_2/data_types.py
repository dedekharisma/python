"""
Primitive data types in Python
1. Strings
2. Integers
3. Floats
4. Booleans
"""
# Subscripting
print("Hello"[4])
print("Hello"[-1])

# String
print("123" + "345")

# Integer = whole number
print(123 + 345)

# Large Integer
print(123_456_789)

# Float = Floating Point Number
print(3.14159)

# Boolean
print(True)
print(False)
